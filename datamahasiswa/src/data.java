    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int i = table.getSelectedRow();
        if (i == -1){
            return;
        }
        String nim = (String) table.getValueAt(i, 0);
        txtnim.setText(nim);
        String nama = (String) table.getValueAt(i, 1);
        txtnama.setText(nama);
        String jurusan = (String) table.getValueAt(i, 2);
        cbjurusan.setSelectedItem(jurusan);
        String kelas = (String) table.getValueAt(i, 3);
        cbkelas.setSelectedItem(kelas);
        String jenis_kelamin = (String) table.getValueAt(i, 4);
        cbjenis_kelamin.setSelectedItem(jenis_kelamin);
        String alamat = (String) table.getValueAt(i, 5);
        txtalamat.setText(alamat);
    }//GEN-LAST:event_tableMouseClicked

