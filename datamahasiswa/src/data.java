    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        String nim = txtnim.getText();
        String nama = txtnama.getText();
        String jurusan = (String) cbjurusan.getSelectedItem();
        String kelas = (String) cbkelas.getSelectedItem();
        String jenis_kelamin = (String) cbjenis_kelamin.getSelectedItem();
        String alamat = txtalamat.getText();
        try{
            Statement statement = (Statement)konek.getConnection().createStatement();
            statement.executeUpdate("INSERT INTO tbmahasiswa VALUES ('"+ nim +"','"+ nama +"','"+ jurusan +"','"+ kelas +"','"+ jenis_kelamin +"','"+ alamat +"' );");
            statement.close();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
        }catch(Exception t){
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan");
        }
        datatable();
    }//GEN-LAST:event_btnsimpanActionPerformed

