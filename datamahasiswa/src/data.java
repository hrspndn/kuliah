    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        if (btnedit.getText() == "btnedit"){
            txtnim.requestFocus();
        }else{
            String nim = txtnim.getText();
            String nama = txtnama.getText();
            String jurusan = (String)cbjurusan.getSelectedItem();
            String kelas = (String) cbkelas.getSelectedItem();
            String jenis_kelamin = (String) cbjenis_kelamin.getSelectedItem();
            String alamat = txtalamat.getText();
            try{
                Statement statement = (Statement)konek.getConnection().createStatement();
                statement.executeUpdate("UPDATE tbmahasiswa SET nim='"+ nim +"',"+" nama ='"+nama+"',"+" jurusan='"+jurusan+"',"+" kelas='"+kelas+"',"+" jenis_kelamin='"+jenis_kelamin+"',"+" alamat='"+alamat+"'" + "WHERE nim = '"+nim+"' OR nama = '"+nama+"'");
                statement.close();
                JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            }catch(Exception t){
                JOptionPane.showMessageDialog(null, "Data Gagal Diubah");
            }
            datatable();
        }
    }//GEN-LAST:event_btneditActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
       String nim = txtnim.getText();
       String nama = txtnama.getText();
       try{
           Statement statement = (Statement)konek.getConnection().createStatement();
           statement.executeUpdate("DELETE FROM tbmahasiswa WHERE nim='"+ nim +"' OR nama='"+ nama +"';");
           JOptionPane.showMessageDialog(null,"Data Berhasil Dihapus");
           txtnim.setText("");
           txtnama.setText("");
           cbjurusan.setSelectedItem("Sistem Informatika");
           cbkelas.setSelectedItem("Reguler Pagi");
           cbjenis_kelamin.setSelectedItem("Laki-laki");
           txtalamat.setText("");
           txtnim.requestFocus();
       }catch(Exception t) {
           JOptionPane.showMessageDialog(null, "Data Gagal Dihapus");
       }
       datatable();
    }//GEN-LAST:event_btnhapusActionPerformed

